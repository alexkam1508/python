import math

print(1 + 3)
print(1 - 3)
print(1 * 3)
print(1 / 3)
print(1 // 3) #Divicion pero te la solo la parte entera 
print(1 % 3) #Modulo de la divicion: te da el residuo de la divicion
print(2 ** 3) #Elevacion del numero
print(round(1.4)) #redondea el numero
print(abs(-1)) #Devuelve el valor absoluto de un numero

print(max(1,2,3,4,5)) #Devuelve el valor maximo

print(math.ceil(1.1))  #Devuelve el valor entero mas grande que el numero

print(math.floor(1.999999))  #Devuelve el valor minimo

print(math.isnan(23))  #Devuelve True si el valor es NaN (Not a Number)

print(math.pow(10, 3))  #Eleva un numero a una potencia

print(math.sqrt(9))  #Devuelve la raiz cuadrada de un numero

